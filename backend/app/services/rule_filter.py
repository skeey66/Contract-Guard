def _normalize(text: str) -> str:
  """공백·줄바꿈 정규화."""
  return re.sub(r"\s+", " ", text).strip()


def check_safe_rule(clause: Clause, contract_type: str = "lease") -> tuple[bool, str]:
  """조항이 확정 safe 패턴에 해당하는지 검사.

  도메인별 정형 표현(법률 본문과 동일·근사)을 결정적 safe 처리한다.
  safe 처리하더라도 추가 부담 표현(즉시/통보 없이/이의제기 불가 등)이
  본문에 있으면 safe 무효화하여 false-safe를 방지한다.

  Returns:
    (is_safe, 매칭된 규칙 설명). 매칭되지 않으면 (False, "").
  """
  patterns = _SAFE_PATTERNS_BY_TYPE.get(contract_type)
  if not patterns:
    return False, ""

  body = _normalize(clause.content)

  # 1) 도메인 공통 — 추가 부담 표현이 있으면 safe 확정 차단
  for veto in _COMMON_LAW_VERBATIM_VETO:
    if veto in body:
      return False, ""

  # 2) lease 전용 veto (기존 — 차임증액 무제한, 임대인 일방결정권 등)
  if contract_type == "lease":
    for veto in _LEASE_SAFE_VETO_KEYWORDS:
      if veto in body:
        return False, ""

  # 3) 도메인별 safe 패턴 매칭
  for pattern, reason in patterns:
    if re.search(pattern, body):
      return True, reason

  return False, ""


_HIGH_PATTERNS_BY_TYPE: dict[str, list[tuple[str, str, str]]] = {
  "lease": _LEASE_HIGH_PATTERNS,
  "service": _SERVICE_HIGH_PATTERNS,
  "loan": _LOAN_HIGH_PATTERNS,
}


def check_high_rule(clause: Clause, contract_type: str = "lease") -> tuple[bool, str, str, str]:
  """조항이 확정 high 패턴에 해당하는지 검사.

  Returns:
    (is_high, 위험 유형, 설명, 매칭된 원문 substring).
    매칭되지 않으면 (False, "", "", ""). 매칭 substring은 frontend 형광펜 표시용.
  """
  patterns = _HIGH_PATTERNS_BY_TYPE.get(contract_type)
  if not patterns:
    return False, "", "", ""

  body_norm = _normalize(clause.content)

  for pattern, risk_type, reason in patterns:
    m = re.search(pattern, body_norm)
    if not m:
      continue
    # 정규화된 본문에서 매칭된 substring을 그대로 quote로 사용한다.
    # 원본 본문에 substring으로 존재하면 그대로 채택, 아니면 정규화 형태 사용
    matched = m.group(0)
    if matched in clause.content:
      quote = matched
    else:
      quote = matched  # frontend의 _validate_quote가 공백 정규화로 매칭함
    return True, risk_type, reason, quote

  return False, "", "", ""
