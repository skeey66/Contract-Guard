"""결정적 룰 기반 조항 분류 레이어.

LLM이 borderline 조항에서 hallucination으로 판정을 흔드는 문제를 완화하기 위해,
LLM 호출 전/후에 명백한 safe/high 패턴을 결정적으로 검사한다.

설계 원칙:
- 특정 계약서나 샘플에 맞추지 않고, 임대차 법리의 일반 원칙에 기반한 패턴만 사용.
- 과도한 매칭은 오히려 false-positive를 만들므로, **확실한 경우만** 강제 교정.
- 애매한 조항은 손대지 않고 LLM 판정을 그대로 사용.
"""

import re
from backend.app.models.clause import Clause


# ────────────────────────────────────────────────
# safe 확정 패턴 (임대차)
# ────────────────────────────────────────────────
# 각 패턴은 (정규식, 설명) 튜플. 조항 본문(제목 제외)에 매칭되면 safe 확정.
# 단, "위험 키워드"가 있으면 safe 확정 해제한다 (복합 조항 대비).

_LEASE_SAFE_PATTERNS: list[tuple[str, str]] = [
  # 목적물 식별 (주소·면적·용도) — 순수 기재 사항만
  (r"소재지\s*:.*면적\s*:", "목적물 식별 기재"),
  # 임대 조건 단순 기재 — 보증금·차임 금액만 명시한 trivial 조항
  # (예: "갑은 위 주택을 을에게 보증금 X원, 월 차임 Y원에 임대한다")
  (
    r"보증금[^.]{0,80}(?:차임|월세|월\s*차임)[^.]{0,80}임대(?:한다|함|하기로)",
    "단순 임대 조건 기재",
  ),
  # 전세 단순 기재 — 차임 키워드 없이 "보증금 X원에 임대" 형태도 포함
  # (예: "갑은 위 주택을 을에게 보증금 사억원정에 임대하며")
  (
    r"보증금[^.]{0,80}임대(?:한다|함|하기로|하며|하고)",
    "단순 임대 조건 기재",
  ),
  # "보증금: 금액 / 차임: 금액 / ... 지불|지급" 형태의 금액·지급 일정 단순 기재
  (
    r"보증금\s*[:：][^.\n]{0,80}(?:월\s*)?차임\s*[:：][^.\n]{0,100}(?:지불|지급)",
    "보증금·차임 금액 및 지급 일정 기재",
  ),
  # 차임 증액 연 5% 이내 (주택임대차보호법 제7조)
  (r"연\s*5\s*%\s*(?:를|이내|이하|를\s*초과하지\s*않)", "법정 증액 한도 준수"),
  (r"주택임대차보호법.*연\s*5\s*%", "법정 증액 한도 준수"),
  # 자연마모 원상복구 제외 (판례)
  (r"(?:통상적인\s*사용|자연\s*마모|경년\s*변화).*제외", "자연마모 제외"),
  # 임대인 주요 수선 + 임차인 소규모 수선 분담 (민법 제623조)
  (
    r"임대인.*(?:주요|구조|설비|보일러|배관|누수).*수선.*임차인.*(?:소모품|소규모)",
    "법정 수선 의무 분담",
  ),
]

# safe 확정을 무효화하는 위험 키워드 (복합 조항에서 high 조항이 섞여 있을 때)
_LEASE_SAFE_VETO_KEYWORDS: list[str] = [
  "무기한",
  "연기할 수",
  "연기 기간",
  "후순위",
  "사전 통보 없이",
  "사전통보 없이",
  "임대인의 결정에 따른",
  "이의제기",
  "이의를 제기할 수 없",
  "모든 수리",
  "원인 불문",
  "즉시 퇴거",
  "묵시적 갱신 적용 안",
  "갱신요구권을 행사하지 않",
  "일방적으로 공제",
  "일방 공제",
  "6개월",
  "6 개월",
]


# ────────────────────────────────────────────────
# high 확정 패턴 (임대차)
# ────────────────────────────────────────────────

